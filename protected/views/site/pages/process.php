        <h3>Прототип</h3>

        <p>Прототип позволяет увидеть структуру и функции сайта до начала основных работ. Проще говоря это - <strong>полностью
            рабочий сайт без дизайна</strong>. Разрабатывается с
            помощью специальных программ на основе ваших пожеланий. Это значительно снижает <strong>время разработки
                сайта</strong>, в случае ошибки, на
            всех этапах. </p>
        <p><strong>Говоря о любом проекте, важно помнить, какие цели и задачи он помогает вам решить!</strong></p>

        <div id="morePrototype" class="collapse out">
            <p>Для небольших проектов существуют <strong>типовые решения</strong>, тем самым можно избежать составления подробнейшего
                            <strong>технического задания</strong> на сайт.</p>

            <p>Немного о типовых решениях.</p>

            <p><strong>Сайт-визитка</strong>: представляет вашу компанию или бизнес. Состоит из нескольких страниц с важной информацией.
            В идеале дизайн должен адаптироваться для всех устройств, будь то: телефон, планшентник или обычный офисные компьютер.</p>

            <p><strong>Промо-сайт</strong>: яркая презентация продукта или маркетинговой компании. Акцент делается на графическом
                представлении(веб-дизайн).</p>

            <p><strong>Корпоративный: </strong>Представляет подробнейшую информацию о вашей компании. Подходит для компаний, которые
            уже окрепли, и готовы дальше развиваться. Корпоративный сайт служит уже не только "описанием", он может служить инструментом
            решения ваших задач! Примерные задачи выглядят так:
                <ul>
                    <li>Представительство в сети интернет.</li>
                    <li>Информация для клиентов.</li>
                    <li>Информация для партнеров.</li>
                    <li>Площадка для сбыта продукции.</li>
                    <li>Взаимодействие между филиалами.</li>
                </ul>
            </p>

            <p>Дальше посложнее:</p>

