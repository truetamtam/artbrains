            ничего не продаст. Проект интернет магазина требует наиплотнейшего сотрудничества разработчика и клиента. Чем больше будет задано
            вопросов в начале, тем больше принесет прибыли ваш будущий интернет-магазин. В случае с магазином в сети,
            <strong>прототип</strong> становится критичным пунктом. Очень важно понять в деталях, как будет работать ваш сайт. Прототип, в этом
            вопросе, окажет неоценимую помощь.</p>

            <p>С помощью прототипа можно увидеть как будет работать ваш сайт, в этом его задача.</p>

